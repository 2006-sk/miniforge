/**
 * Writes cloud-functions/_lib/runtime-env.js from local .env for Makers deploy.
 * EdgeOne CLI env set is not applying; this keeps keys in the function bundle.
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const envPath = resolve(root, '.env');
const outPath = resolve(root, 'cloud-functions/_lib/runtime-env.js');

const keys = [
  'NEBIUS_API_KEY',
  'SERPAPI_API_KEY',
  'SERPER_API_KEY',
  'MAKERS_MODELS_KEY',
  'TEXT_MODEL',
  'NEBIUS_TEXT_MODEL',
  'VISION_MODEL',
];

const values = Object.create(null);
if (existsSync(envPath)) {
  for (const line of readFileSync(envPath, 'utf8').split('\n')) {
    const m = line.match(/^([A-Z0-9_]+)=(.*)$/);
    if (!m) continue;
    const [, k, v] = m;
    if (keys.includes(k)) values[k] = v.trim();
  }
}

// Alias
if (!values.SERPAPI_API_KEY && values.SERPER_API_KEY) {
  values.SERPAPI_API_KEY = values.SERPER_API_KEY;
}

const body = `/** Auto-generated by scripts/inject-runtime-env.js — do not edit. */
export const RUNTIME_ENV = ${JSON.stringify(values, null, 2)};
`;

writeFileSync(outPath, body);
console.log('[inject-runtime-env] wrote', outPath, 'keys:', Object.keys(values).join(', '));
